function formatActiveServiceOperators() {
  return SERVICE_OPERATORS.filter((o) => o.status === "active")
    .map(
      (o, index) =>
        `${index + 1}) ${o.legalName}, ИНН ${o.inn}, ОГРНИП ${o.ogrnip}, ${o.activity}, адрес: ${o.address}`,
    )
    .join("\n\n");
}

function formatOperatorContacts() {
  const lines = [
    `Оператор приложения (PWA):\n${APP_OPERATOR.legalName}, ИНН ${APP_OPERATOR.inn}, ОГРНИП ${APP_OPERATOR.ogrnip}\nАдрес: ${APP_OPERATOR.address}\nE-mail: ${APP_OPERATOR.email}, тел.: ${APP_OPERATOR.phone}`,
  ];

  SERVICE_OPERATORS.filter((o) => o.status === "active").forEach((o) => {
    lines.push(
      `${o.activity}:\n${o.legalName}, ИНН ${o.inn}, ОГРНИП ${o.ogrnip}\nАдрес: ${o.address}`,
    );
  });

  return lines.join("\n\n");
}

